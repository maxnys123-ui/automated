// 16 Dec 2025: While Loop

let number = 5;

// Simple while loop
while (number > 0) {
    console.log('Number:', number);
    number--;
}

// While loop with condition
let count = 0;
while (count < 3) {
    console.log('Count:', count);
    count++;
}

// While loop with flag
let running = true;
let iteration = 0;
while (running && iteration < 5) {
    console.log('Iteration:', iteration);
    iteration++;
    if (iteration === 3) {
        running = false;
    }
}





